public class PersonalityTrait {

    public static void ExtrovertTrait(){
        System.out.println("You have an extrovert personality\nTypically outgoing and enjoy socializing\n");
    }
    
    public static void IntrovertTrait(){
        System.out.println("You have an introvert personality\nTypically shy and reticent, prefer to stay at home\n");
    }

    public static void IEMiddleTrait(){
        System.out.println("You are somewhere in the middle of the extroversion-introversion\n");
    }

    public static void OptimistTrait(){
        System.out.println("You are an optimist person\nYou always stay happy no matter what\nTypically outgoing and enjoy socializing\n");
    }

    public static void PessimistTrait(){
        System.out.println("You are a pessimist person\nLife is one rainy day after another for you, you always expect worse result\n");
    }

    public static void OPMiddleTrait(){
        System.out.println("You are in between of an optimist and a pessimist\nYou are bright most of the time but also feels a bit down in feeling\n");
    }
}
